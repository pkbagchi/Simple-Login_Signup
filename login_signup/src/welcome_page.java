    public void login(){
        if(tuser.getText().length()==0 || tpass.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Wrong user name or password!","Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
            
        String user=tuser.getText();
        String pass=tpass.getText();
      
        try {
            if(canLogin(user,pass)){
                
             JOptionPane.showMessageDialog(this, "Login successful!");
               //new welcome_page().setVisible(true);
               
                close();
            }
            else{
                JOptionPane.showMessageDialog(null,"Wrong user name or password!","Error", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Connection Problem!","Error", JOptionPane.INFORMATION_MESSAGE);
        
        }
    
    }
