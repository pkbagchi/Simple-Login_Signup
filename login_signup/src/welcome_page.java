    public boolean canLogin(String user,String pass) throws ClassNotFoundException, SQLException{
        
        
        
       String query = "SELECT * FROM `info` WHERE 1";
       Class.forName("com.mysql.jdbc.Driver");
       Connection connection = (Connection)DriverManager.getConnection(url,dusername,dpassword);
       Statement statement = (Statement) connection.createStatement();
   
       ResultSet result;
       result = statement.executeQuery(query);
       while(result.next()){
        String duser = result.getString("user");
        String dpass = result.getString("pass");
       
        if(duser.equals(user)){
            if(dpass.equals(pass)){
              userId=result.getString("user");
              name=result.getString("name");
             
               statement.close();
                return true;
            }
        }
       }
       
       
       
       statement.close();
       return false;
       
        
    }
        
    
